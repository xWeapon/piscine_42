char	*ft_strupcase(char *str)
{
	char *p_str;

	p_str = str;
	while (*p_str)
	{
		if (*p_str >= 'a' && *p_str <= 'z')
			*p_str -= 32;
		p_str++;
	}
	return (str);
}
